/**
 * 最低1kg 起收费1kg以上0. 5kg为单位
 * 6.1收6.5
 * 6.6收7
 */
export function getWholeWeight(w) {
    if (!w) return 0
    if (w < 0) return 0
    if (Number.isInteger(w)) return w
    if (w < 1) return 1
    w = w + ''
    let splitNum = w.split('.')
    let mainNum = splitNum[0]
    let firstNum = splitNum[1].substr(0, 1)
    if (firstNum <= 5) return parseFloat([mainNum, 5].join('.'))
    else return parseInt(mainNum) + 1
}

/**
 * 将1.020020303转成1.02
 * @param {*} n 
 */
export function getFixed2Num(n) {
    n = parseFloat(n)
    n = n.toFixed(2)
    return parseFloat(n)
}

export function calcVolumn({ width, length, height }) {
    return getFixed2Num(width * length * height / 6000) || 0
}


